        <h2>Interests</h2>
        <p>
            Curriculum Vitae Format: Quick Tips
        </p>
        <h3>CV Length:</h3>
        <p>While resumes are generally one page long, CVs are longer. Most CVs are at least two pages long, and often
            much longer.</P>

        <h3>Font and Size</h3>
        <p>Do not use ornate fonts that are difficult to read; Times New Roman, Arial, Calibri, or a similar font is
            best.</p>

        <p>Your font size should be between 10 and 12 points, although your name and the section headings can be a
            little larger and/or bolded.</p>

        <h3>Format:</h3>
        <p>However you decide to organize the sections of your CV, be sure to keep each section uniform. For example, if
            you put the name of one organization in italics, every organization name must be in italics.
        </p>

        <h3>Accuracy:</h3>
        <p>Be sure to edit your CV before sending it. Check spelling, grammar, tenses, names of companies and people,
            etc. Have a friend or career services counselor check over your CV as well.
        </p>
        <h2>Curriculum Vitae Format: What to Include</h2>

        <p>Not all CVs look the same. You may choose to include only some of these sections because others do not apply
            to your background or your industry. Include what seems appropriate for your area of specialty.
        </p>
        <h3>Contact information:</h3>
        <p>At the top of your CV, include your name and contact information (address, phone number, email address,
            etc.).</p>

        <p>Outside of the US, many CVs include even more personal information, such as gender, date of birth, marital
            status, and even names of children. Unless you are applying to a job outside of the United States, you do
            not need to include this extra information.</P>

        <h3>Education:</h3>
        <p>This may include college and graduate study. Include the school attended, dates of study, and degree
            received.</p>

        <h3>Honors and Awards:</h3>
        <p>This may include dean's list standings, departmental awards, scholarships, fellowships, and membership in any
            honors associations.</p>

        <h3>Thesis/Dissertation:</h3>
        <p> your thesis or dissertation title. You may also include a brief sentence or two on your paper, and/or the
            name of your advisor.</p>

        <h3>Research Experience:</h3>
        <p>List any research experience you have, including where you worked, when, and with whom. Include any
            publications resulting from your research.</p>

        <h3>Work Experience:</h3>
        <p>List relevant work experience; this may include non-academic work that you feel is worth including. List the
            employer, position, and dates of employment. Include a brief list of your duties and/or accomplishments.</p>

        <h3>Teaching Experience:</h3>
        <p>List any teaching positions you have held. Include the school, course name, and semester. You may also
            include any other relevant tutoring or group leadership experience.</p>

        <h3>Skills:</h3>
        <p>List any relevant skills you have not yet mentioned. This may include language skills, computer skills,
            administrative skills, etc.</p>

        <h3>Publications and Presentations:</h3>
        <p> List any publications you have written, co-written, or contributed to. Include all necessary bibliographic
            information. You should also include any pieces you are currently working on. Include papers you presented
            at conferences and/or associations: list the name of the paper, the conference name and location, and the
            date.</p>

        <h3>Professional Memberships:</h3>
        <p> List any professional associations to which you belong. If you are a board member of the association, list
            your title.</p>

        <h3>Extracurricular Activities:</h3>
        <p> Include any volunteer or service work you have done, as well as any clubs or organizations to which you have
            belonged. You can also include any study abroad experiences here if you have not already mentioned them.</p>
